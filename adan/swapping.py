num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

print("Before Swapping")
print("First Number: ", num1)
print("Second Number: ", num2)

temp = num1
num1 = num2
num2 = temp

print("After Swapping")
print("First Number: ", num1)
print("Second Number: ", num2)
